package Operators_02;

//its like a comparision operators
public class RelationalOp02 {

	public static void main(String[] args) {
		int a = 5;
		int b = 7;
		
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
	
	
}
